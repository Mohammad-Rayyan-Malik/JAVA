class relationaloperator
// relationaloperator give only true and false answer 
// == != etc
 {
	public static void main(String[]args)
	{
	
	int amit=100;
	int samir=20;
		System.out.println(amit>100);
		System.out.println(amit>=100);
		System.out.println(amit!=100);
		System.out.println(amit>samir);
		System.out.println(amit=samir);
		System.out.println(amit==samir);
	}
 }