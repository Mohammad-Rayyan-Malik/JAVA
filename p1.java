class p1{
	public static void main(String []args)
	{
		
		//unary operator it's deals with only one operand always
		// ++ increament    -- decrement
		
		
		// increament
		int i=10;
		i++;
		System.out.println(i);
		
		
		//decreament
		int j=10;
		j--;
		j--;
		i--;
		j--;
		j++;
		i++;
		System.out.println(j+i);
		
		// if write ++ before variable it means pre inreament
		// if write ++ after variable it means post inreament
		// if write -- before variable it means pre decrement
		// if write -- after variable it means pro decrement
		
		System.out.println(++i);
		System.out.println((++j)+(++i));
		System.out.println((++j)+(++i));
		
		
		
	}
	
	
}